package commands;

/**
 * Интерфейс, който дефинира команден шаблон за всички операции в системата за билети.
 * Всеки клас, който имплементира този интерфейс, трябва да предостави конкретна
 * реализация на метода {@code execute}.
 */
public interface Command {

    /**
     * Метод за изпълнение на конкретна команда, базирана на подадени аргументи.
     * @param args Масив от аргументи, подадени от потребителя чрез главното меню.
     */
    void execute(String[] args);
}
